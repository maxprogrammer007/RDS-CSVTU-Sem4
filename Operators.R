## Operators in R

## Arithmetic ops = + - * / %%(remainder) %/%(quotient) ^
## Relational ops
## Logical ops
## Assignment ops

a<-7.5
b<-2

print(a+b) # Additiom
print(a-b) # substract
print(a*b) # Multiplicatin
print(a/b) #Division
print(a%%b) #Remainder
print(a%/%b) # quotient
print(a^b) # Power

